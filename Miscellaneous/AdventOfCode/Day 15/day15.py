def Djikstra(grid):
    from heapq import heappop, heappush
    from collections import deque
    rows, cols = len(grid), len(grid[0])
    d = [(1,0),(0,1),(-1,0),(0,-1)]
    distances = [[None] * cols for _ in range(rows)]

    q = [(0,0,0)]

    while q:
        distance, r, c = heappop(q)

        if distances[r][c] is None or distance + grid[r][c] < distances[r][c]:
            distances[r][c] = distance + grid[r][c]
        else:
            continue
            
        if r == rows - 1 and c == cols - 1:
            break

        for dr, dc in d:
            rr,cc = r + dr, c + dc
            if 0 <= rr < rows and 0 <= cc < cols:
                heappush(q, (distance + grid[r][c], rr, cc))
    return distances[rows - 1][cols - 1] - grid[0][0]


